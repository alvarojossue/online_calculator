class Calculator
	def add(first_number, second_number)
		result = first_number.to_f + second_number.to_f
	end

	def substract(first_number, second_number)
		result = first_number.to_f - second_number.to_f
	end

	def multiply(first_number, second_number)
		result = first_number.to_f * second_number.to_f
	end

	def divide(first_number, second_number)
		result = first_number.to_f / second_number.to_f
	end

end
